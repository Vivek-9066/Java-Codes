package com.java;

public class half_diamonnd {

	public static void main(String[] args) {
		int i,j; //simple pattern in increasing order 
		for(i=1;i<=10;i++)
		 
		{
			for(j=1;j<=i;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
		for(i=9;i>=1;i--) // simple star pattern in decreasing order
		{
			for(j=1;j<=i;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
		
		// combine upper 2 code to make vertical half diamond pattern
		
		

	}

}
